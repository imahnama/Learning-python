# Tuples are a sequence data types
# It consists of a number of values separated by commas
# Tuples can hold diff types of data types
# They are immutable - cannot be added new items

t = (1, 2, 3, 4)
print(len(t)) # print length of the Tuple

# concatenation
z = t + (5,5)
print(z)

#slicing
print (z[1])

#Tuples also have callable methods

y = 'nama', 3.0, [1, 2, 3]
print (y[1])
